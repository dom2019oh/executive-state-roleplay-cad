async function seedPenalCodes() {
  console.log(`\n[3/3] Seeding penal_codes (${PENAL_CODES.length} documents) …`)
  const col = db.collection('penal_codes')
  for (const group of chunks(PENAL_CODES, 490)) {
    let batch = db.batch()
    let count = 0
    for (const entry of group) {
      const docId = entry.code.replace(/[\s/]/g, '_')
      batch.set(col.doc(docId), entry, { merge: true })
      count++
    }
    await flushBatch(batch, `${count} penal_codes`)
  }
  console.log(`  ✓ ${PENAL_CODES.length} penal_codes seeded`)
}

