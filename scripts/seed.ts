async function main() {
  console.log('╔══════════════════════════════════════════╗')
  console.log('║  Executive State CAD — Firestore Seeder  ║')
  console.log('╚══════════════════════════════════════════╝')
  const start = Date.now()

  await seedSystemConfig()
  await seedTenCodes()
  await seedPenalCodes()

  const elapsed = ((Date.now() - start) / 1000).toFixed(2)
  console.log(`\n✅ Seed complete in ${elapsed}s`)
  console.log(`   system_config: 1 doc`)
  console.log(`   ten_codes:     ${TEN_CODES.length} docs`)
  console.log(`   penal_codes:   ${PENAL_CODES.length} docs`)
  process.exit(0)
}

main().catch((err) => {
  console.error('Seed failed:', err)
  process.exit(1)
})
