async function seedTenCodes() {
  console.log(`\n[2/3] Seeding ten_codes (${TEN_CODES.length} documents) …`)
  const col = db.collection('ten_codes')
  for (const group of chunks(TEN_CODES, 490)) {
    let batch = db.batch()
    let count = 0
    for (const entry of group) {
      const docId = entry.code.replace(/[\s/]/g, '_')
      batch.set(col.doc(docId), entry, { merge: true })
      count++
    }
    await flushBatch(batch, `${count} ten_codes`)
  }
  console.log(`  ✓ ${TEN_CODES.length} ten_codes seeded`)
}

